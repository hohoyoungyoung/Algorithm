mod = 10007
N = int(input())

# N=1일 때 각 자릿수별 오르막 수의 개수는 모두 1 (0~9까지)
dp = [1] * 10

# 이미 한 자릿수(길이 1)는 구해둔 상태이므로, N-1번 자릿수를 추가한다.
for _ in range(N - 1):
    # 뒤에서부터 누적합을 구한다.
    # dp[i] = dp[i] + dp[i+1]  (i=8,7,...,0 순으로 갱신)
    for i in range(8, -1, -1):
        dp[i] = (dp[i] + dp[i + 1]) % mod

# 길이가 N인 오르막 수의 총 개수는 마지막 자리 숫자에 관계없이 모두 더한 값이다.
print(sum(dp) % mod)
